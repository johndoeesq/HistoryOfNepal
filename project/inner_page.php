                                <p class="title">Sagamartha (Everest) Base Camp trek</p>

                                <p>It takes us into one of the most spectacular regions
                                    of Nepal where the Sherpa culture thrives amongst the highest peaks in the world.
                                    The Sagamartha massif as seen from Thyangboche and Kala Pattar is a sight you will
                                    never forget, and the hospitality of these tough mountain people will remain with
                                    you for a long time. This is the traditional trek to view Sagamartha, following the
                                    historic trail and travelling in true expedition style; it will appeal to those who
                                    wish to retrace the steps of history to the base of the highest mountain in the.

                                    Commencing from the airstrip at Lukla the first three days are spent trekking the
                                    established trail to E.B.C. We traverse West for a gradual ascent to the beautiful
                                    five lakes of Gokyo,where we rest for some side treks including a climb of Gokyo Ri
                                    for some fantastic views. From Gokyo we trek over the Chola pass onwards to Gorak
                                    Shep on the main trail and Kala Pattar, fantastic views of Everest .A further five
                                    days are spent descending to Lukla for our flight to Kathmandu.

